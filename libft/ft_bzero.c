#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	c;
	char	*ps;

	i = 0;
	c = '\0';
	ps = s;
	while (i != n)
	{
		ps[i] = c;
		i++;
	}
}
