#include "../minishell.h"

int		check_symbol(char c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z') || (c == '_') ||
	(c >= '0' && c <= '9'))
		return (1);
	return (0);
}

int		check_valid(t_par *par, int *j, int *i)
{
	if ((par->time_str[*j][*i] == '=' && *i == 0) ||
	(par->time_str[*j][*i] == '\0' && *i == 0))
		return (1);
	else
		return (0);
}
